            <h3>We are glad you visited Cma page</h3>
            <h3><mark>Brief Introduction</mark></h3>
            The Catholic Men Association, abbreviated CMA is a solidarity group that brings together all practising Catholic men to take an active role in the process of evangelisation in the home,
             the SCC, the church and the country in general.
             <h3><mark>Objectives</mark></h3>
             <ol>
                 <li>To bring together and unite Catholic men in Kenya.</li>
                 <li>To evangelize the Gospel of our Lord Jesus Christ and defend the Catholic faith.</li>
                 <li>To promote the Catholic faith and religious life among members.</li>
                 <li>To provide means for co-operation among Catholic men and the expression of their collective opinions, views and decisions upon matters affecting Christian morality, family and civic life.</li>
                 <li>To participate in matters calculated to lead to the strengthening of the Catholic Church, including Christian Communities.</li>
                 <li>To promote the education and Christian spirituality of Catholic Men and their families.</li>
                 <li>To promote effective representation of the Association in church, the public and social welfare organizations where such representation will serve the objectives of the Association.</li>
                 <li>To promote healthy social behaviour of members and care for environment.</li>
                 <li>To identify and assist the members of the Association in difficult circumstances;
                    eg the aged, sick, poor, destitute, orphans, HIV infected and affected, street families, widows, single- mothers and others alike.</li>
                 <li>To co-operate and network with the other Church associations, the government and other organizations with similar aspirations.</li>
                 <li>To enhance solidarity and participation of Catholic Men in the Republic of Kenya in the fulfilment of the objectives of the association.</li>
                 <li>To co-operate with other associations or organizations within or outside Kenya having objectives similar to the associations.</li>
                 <li>To encourage and assist members to form socio-economic initiatives to promote self-reliance.</li>
                 <li>To acquire either by purchase, lease or otherwise, any movable or immovable property or other assets and sell, let, mortgage, charge or otherwise deal with or dispose them.</li>
                 <li>To seek and obtain legal advice and any assistance on any matters affecting the Association or for protecting the rights of a member on matters arising out of the relations with the Church or State. </li>
             </ol>
             
            
            
            
